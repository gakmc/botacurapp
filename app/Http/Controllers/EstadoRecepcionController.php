    {
        EstadoRecepcion::create([
            'reserva_id' => $request->reserva_id,
            'user_id' => auth()->id(),
        ]);

        event(new \App\Events\Reserva\Recepcionada(
            $request->reserva_id,
            auth()->user()->name
        ));

        return response()->json([
            'message' => 'Cliente recepcionado correctamente',
            'user_name' => auth()->user()->name,
        ]);
    }

